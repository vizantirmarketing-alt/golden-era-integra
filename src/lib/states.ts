export const INTERNATIONAL = { code: "INTL", name: "International" } as const;

export const ALL_REGIONS = [...US_STATES, INTERNATIONAL] as const;

export type StateCode = (typeof ALL_REGIONS)[number]["code"];

export const VALID_STATE_CODES: ReadonlySet<string> = new Set(
  ALL_REGIONS.map((r) => r.code),
);

export function getStateName(code: string | null | undefined): string {
  if (!code) return "Unknown";
  const match = ALL_REGIONS.find((r) => r.code === code);
  return match ? match.name : code;
}
